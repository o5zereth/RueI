namespace RueI.Utils.Enums;

/// <summary>
/// Represents the direction to add margins.
/// </summary>
public enum MarginDirection
{
    /// <summary>
    /// The margins should be added to the left.
    /// </summary>
    Left,

    /// <summary>
    /// The margins should be added on both the left and right.
    /// </summary>
    Center,

    /// <summary>
    /// The margins should be added on the right.
    /// </summary>
    Right,
}